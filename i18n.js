/** 対応言語コードの一覧（表示順） */
const SUPPORTED_LANGS = ['en', 'it', 'es', 'pt', 'ja'];

/** 現在の言語（既定: 英語。localStorage に保存された選択を優先） */
let currentLang = (() => {
  const saved = localStorage.getItem('poc-lang');
  return SUPPORTED_LANGS.includes(saved) ? saved : 'en';
})();

/**
 * 翻訳文字列を取得する。
 * 現在言語 → 英語 → キーそのもの、の順でフォールバックする。
 * @param {string} key 辞書キー
 * @param {Object<string,string|number>} [params] {name} 形式プレースホルダの置換値
 */
function t(key, params) {
  let s = I18N[currentLang]?.[key] ?? I18N.en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.split(`{${k}}`).join(String(v));
    }
  }
  return s;
}

/** data-i18n* 属性を持つ全要素へ現在言語の文言を反映する */
function applyI18nToDom() {
  document.documentElement.lang = currentLang;
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-title]').forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    el.setAttribute('aria-label', t(el.dataset.i18nAria));
  });
}

/** 言語を切り替えて DOM へ反映し、選択を保存する */
function setLanguage(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  currentLang = lang;
  localStorage.setItem('poc-lang', lang);
  applyI18nToDom();
}
